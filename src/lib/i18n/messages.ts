type SharedMessages = typeof enMessages;

function mergeMessages<T>(base: T, override: unknown): T {
  const result: Record<string, unknown> = { ...(base as Record<string, unknown>) };
  const overrideObject = (override ?? {}) as Record<string, unknown>;

  for (const [key, value] of Object.entries(overrideObject)) {
    const baseValue = result[key];
    if (
      value
      && typeof value === "object"
      && !Array.isArray(value)
      && baseValue
      && typeof baseValue === "object"
      && !Array.isArray(baseValue)
    ) {
      result[key] = mergeMessages(baseValue as Record<string, unknown>, value);
    } else if (value !== undefined) {
      result[key] = value;
    }
  }

  return result as T;
}

